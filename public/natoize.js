function natoize(str) {
  let nato = [];

	for (let i = 0; i < str.length; i++) {
    const char = str.charAt(i);

    if (!(char in map)) {
      nato.push(`${char} not found in map`);
      continue;
    }

    nato.push(map[char]);
  }

  return nato.join('<strong>&mdash;</strong>');
}
